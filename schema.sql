UPDATE public.users SET role = 'participant' WHERE role IS NULL;
UPDATE public.users SET zampa_role = 'user' WHERE zampa_role IS NULL;


-- ==========================================
-- 2. TAULA: ZAMPA_EDITIONS (edicions anuals)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.zampa_editions (
    id integer PRIMARY KEY, -- Any de l'edició (p. ex: 2026)
    status text NOT NULL DEFAULT 'open' CHECK (status IN ('open', 'vote', 'closed', 'finished')),
    official_winner_adult text -- Es relacionarà amb clau forana un cop existeixi 'zampa_projects'
);

-- ==========================================
-- 3. TAULA: ZAMPA_PROJECTS (portfolis participants)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.zampa_projects (
    id text PRIMARY KEY,
    edition_year integer REFERENCES public.zampa_editions(id) ON DELETE CASCADE NOT NULL,
    category text NOT NULL DEFAULT 'adult' CHECK (category IN ('adult')),
    author_name text NOT NULL,
    project_title text NOT NULL,
    description text,
    popular_rank_position integer,
    submitted_at timestamptz DEFAULT now()
);

-- S'enllaça el guanyador oficial de 'zampa_editions' un cop 'zampa_projects' ja existeix
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.table_constraints
        WHERE constraint_name = 'fk_winner_adult' AND table_name = 'zampa_editions'
    ) THEN
        ALTER TABLE public.zampa_editions ADD CONSTRAINT fk_winner_adult FOREIGN KEY (official_winner_adult) REFERENCES public.zampa_projects(id) ON DELETE SET NULL;
    END IF;
END $$;

-- ==========================================
-- 4. TAULA: ZAMPA_PHOTOS (fotos de cada projecte)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.zampa_photos (
    id text PRIMARY KEY,
    project_id text REFERENCES public.zampa_projects(id) ON DELETE CASCADE NOT NULL,
    file_url text NOT NULL,
    file_name text,
    photo_title text,
    description text,
    order_index integer NOT NULL
);

-- ==========================================
-- 5. TAULA: ZAMPA_USER_RANKS (rànquings enviats pels socis)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.zampa_user_ranks (
    id integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id text REFERENCES public.users(id) ON DELETE CASCADE NOT NULL,
    project_id text REFERENCES public.zampa_projects(id) ON DELETE CASCADE NOT NULL,
    edition_year integer NOT NULL,
    category text NOT NULL DEFAULT 'adult' CHECK (category IN ('adult')),
    assigned_position integer NOT NULL,
    submitted_at timestamptz DEFAULT now(),
    UNIQUE (user_id, project_id)
);


