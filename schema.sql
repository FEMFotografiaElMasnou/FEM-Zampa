-- ==========================================
-- 1. TAULA: USERS (socis, administradors i editors)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.users (
    id text PRIMARY KEY,
    display_name text NOT NULL,
    email text UNIQUE NOT NULL,
    password text NOT NULL,
    role text NOT NULL DEFAULT 'participant' CHECK (role IN ('admin', 'participant')),
    zampa_role text NOT NULL DEFAULT 'user' CHECK (zampa_role IN ('admin', 'editor', 'user')),
    created_at timestamptz DEFAULT now(),
    submitted_at timestamptz
);

-- Reparació d'emergència per si la taula 'users' ja existia prèviament sense alguna columna
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS display_name text;
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS email text;
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS password text;
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS role text DEFAULT 'participant';
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS zampa_role text DEFAULT 'user';
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now();
ALTER TABLE public.users ADD COLUMN IF NOT EXISTS submitted_at timestamptz;

